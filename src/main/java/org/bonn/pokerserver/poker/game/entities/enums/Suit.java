package org.bonn.pokerserver.poker.game.entities.enums;

/**
 * An enum containing the 4 suits of a card in the following order:
 * HEART, SPADE, CLUB, DIAMOND
 */
public enum Suit {
    /**
     * The heart suit
     */
    HEART,

    /**
     * The spade suit
     */
    SPADE,

    /**
     * The club suit
     */
    CLUB,

    /**
     * The diamond suit
     */
    DIAMOND
}
